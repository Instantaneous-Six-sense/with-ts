function addition(a: number, b: number) {
  return a + b;
}

function pow(a: number, b: number) {
  return Math.pow(a, b);
}

function subtraction(a: number, b: number) {
  return a - b;
}

function multiplication(a: number, b: number) {
  return a * b;
}

function division(a: number, b: number) {
  return a / b;
}

